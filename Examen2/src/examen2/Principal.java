    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        DefaultListModel modelo=(DefaultListModel)lista_computadoras.getModel();
        DefaultTableModel m=(DefaultTableModel)tabla.getModel();
        DefaultListModel n=(DefaultListModel)lista_tecnicos.getModel();
        Tecnico t=((Tecnico)n.getElementAt(lista_tecnicos.getSelectedIndex()));
        t.setCantidad_computadoras(t.getCantidad_computadoras()+1);
        Computadora c=((Computadora)modelo.getElementAt(lista_computadoras.getSelectedIndex()));
        adminBarra ab=new adminBarra(barra, c, tabla);
        ab.start();
        ab.Tabla();
        int s;
        if(t.getCantidad_computadoras()<6)
        {
            s=1+ta.nextInt(100);
            if(s>0 && s<31)
            {
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error de ensamble");
            }
        }
        else if(t.getCantidad_computadoras()>=6 && t.getCantidad_computadoras()<15)
        {
            s=1+ta.nextInt(100);
            if(s>0 && s<22)
            {
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error de ensamble");
            }
        }
        else if(t.getCantidad_computadoras()>=15 && t.getCantidad_computadoras()<31)
        {
            s=1+ta.nextInt(100);
            if(s>0 && s<13)
            {
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error de ensamble");
            }
        }
        else if(t.getCantidad_computadoras()>=31)
        { 
            s=1+ta.nextInt(100);
            if(s>0 && s<7)
            {
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error de ensamble");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

