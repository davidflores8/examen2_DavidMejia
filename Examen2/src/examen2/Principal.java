        // TODO add your handling code here:
        DefaultComboBoxModel modelo=(DefaultComboBoxModel)cb_ram.getModel();
        String tamaño;
        String marca;
        int tiempo;
        String id;
        id=id_nueva_ram.getText();
        tamaño=(String) tamano_nueva_ram.getValue().toString();
        marca=marca_nueva_ram.getText();
        tiempo=(int) tiempo_nueva_ram.getValue();
